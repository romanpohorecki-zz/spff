		<section class='section-news'>
			<h2>“CALL ME MARIANNA” – AN INTERVIEW WITH CINEMATOGRAPHER KACPER CZUBAK</h2>
			<img src='../images/news/kacper.jpg'>
			<p>“Call Me Marianna ” is an award-winning documentary. This story is told by a 40-year-old Polish woman, who finally decides to fight for her freedom. She must sue her parents in court, because this is the only way to legally change her gender. The film’s cinematographer, Kacper Czubak, agreed to talk to us about this important project. He tells us about Marianna, his work, and what it takes to make a good documentary.<br />
			</p>
			<p><strong>“Call Me Marianna” won many movie festival awards, congratulations! How do 
			you feel about your project’s success? Relief? Did you feel responsible for 
			Marianna’s life and how her story might be perceived? </strong></p>
			<p>Yes, that’s a very interesting question. We take great pride that this movie 
			was enthusiastically received. The public response, particularly the audience 
			awards, has been the best reward for Marianna. She was very happy sharing her 
			work at film festivals and enjoying public recognition. The public response 
			showed that she was accepted as a woman. She wants to be an ordinary woman, an 
			ordinary Marianna, and to be a part of the public crowd; she wants for people to 
			not treat her like something strange. Those awards are proof of her achieving 
			her goal. <br />
			</p>
			<p> <strong>You’ve been working on this documentary for 3 years. Did it make this 
			project special? You’ve worked on many documentaries before – was “Call Me 
			Marianna” the most important of your career?</strong><br />
			</p>
			<p>Yes, we filmed this movie on and off over the course of 3 years. For me, it 
			has been the most important documentary of my career due to the sensitivity and 
			significance of the story. Marianna is the first transsexual person I’ve met, so 
			the experience allowed me to discover a whole new world, a new aspect of 
			humanity. It was an incredible experience. </p>
			<p> <strong>Did Marianna’s life influence your own during those 3 years? Did you 
			try to maintain a separate, professional attitude, or did you become friends 
			with her? </strong></p>
			<p>Sure, we became good friends with Marianna. The film would not have been 
			possible otherwise. Every documentary has its foundation in the relationship 
			between the filmmakers and its central subject. </p>
			<p> <strong>It seems incredibly hard; almost like being a psychologist. </strong>
			</p>
			<p>Exactly! A director is a psychologist for the subject, and the 
			cinematographer is a psychologist for the director. It’s a chain of 
			dependencies. Documentaries are sometimes very hard to make because of the 
			influence they can have on the filmmakers’ lives. When Marianna was sick, 
			Karolina [Karolina Bielawska – the director] was with her at the hospital all 
			the time. On the other hand, it’s very interesting and sometimes can change your 
			life – as happened to me. </p>
			<p> <strong>Since you already mentioned the hospital, were the scenes filmed 
			after the stroke the most challenging emotionally? They were really 
			overwhelming. </strong></p>
			<p>We filmed just two scenes right after Marianna’s stroke. Actually, Karolina 
			made them by herself. They were very emotionally charged and we were considering 
			whether we should add them to the final version of our movie. Karolina asked her 
			if we should stop or if we should keep going and finish what we started. 
			Marianna didn’t want to stop. She agreed to show herself in this difficult 
			situation. She won. We finished our movie. We still can’t believe things turned 
			out this way. It looks tragic. To be honest, we didn’t believe she could 
			overcome her illness. </p>
			<p> <strong>Marianna, as you said, wanted to be just an average woman. How would 
			you interpret her agreement to make a documentary about her life? </strong></p>
			<p>In my opinion it’s because she wanted to be accepted. It doesn’t matter if 
			it’s a public or familial acceptance. As you know, her family didn’t accept her 
			at all. Marianna got her acceptance from the movie’s audience and awards. She 
			thought it would be a relief, not just for her but also for other people who 
			feel the same. </p>
			<p> <strong>How did you manage to paint such a diverse picture of Marianna’s 
			loneliness and her tough battle for a better life? I am talking about both the 
			spiritual and practical aspects: I read that her apartment was only 172 square 
			feet! </strong></p>
			<p>Again, everything begins with intimacy. There must be a relationship and 
			trust between the cinematographer and the subject. This is absolutely a basic 
			thing. And then, when you have this trust… the places where we film are always 
			the biggest inspiration for me. Marianna, in her tiny apartment, had a lot of 
			mirrors. They gave me a lot of opportunity to film her from different angles. 
			Without them, it won’t be possible. Mirrors are a trick I really like and use a 
			lot. </p>
			<p> <strong>Such a creative artist! That’s perhaps why you were chosen for 
			Berlinale Talents. Congratulations. </strong></p>
			<p>Thank you! Yes, I was nominated to Berlinale Talents because of this film.
			</p>
			<p> <strong>Did you feel like you had a mission to work on this project? Did you 
			think that you can change something about the Polish reality? </strong></p>
			<p>Did we feel it’s a mission? You know, there are a lot of things going on 
			during filming. You are taking care of daily tasks. You think about finishing 
			the movie, to tell the truth about the subject. Rather, we didn’t have a feeling 
			that it’s our ambition to change something. We really wanted to tell Marianna’s 
			story. We discovered the social power of the movie when people saw it and they 
			had really good opinions. </p>
			<p> <strong>Perhaps that’s why this movie achieved such a success: because of the 
			truth that shines through it. </strong></p>
			<p>If we had had this attitude, that it must be breakthrough masterpiece that 
			will turn Polish reality upside down, we would have jinxed it. Really, we just 
			focused on our work. </p>
			<p> <strong>Your work took almost three years – how many times did the concept 
			change during that time? How did you come up with the idea that Marianna should 
			talk about herself to actors in a theatre? </strong></p>
			<p>The concept was the same from the beginning. We wanted to show Marianna’s 
			previous life. We were thinking about how to make it interesting, but at the 
			same time we didn’t want to make a feature film. We had neither the desire nor 
			the money for that. We decided to have real actors sitting with Marianna and 
			listening to her. That was the only part we created. </p>
			<p> <strong>It’s an interesting blend of documentary and feature film. </strong>
			</p>
			<p>Actually the questions and conversations were improvisation between Marianna 
			and the actors. The actors didn’t know Marianna before. </p>
			<p> <strong>Has this movie changed your professional life a lot? </strong></p>
			<p>I wouldn’t say it was a big revolution in my career, but it’s true, I’m 
			getting more offers. I got a very interesting opportunity from a producer from 
			the United States. She saw “Call Me Marianna” at one of the festivals in the USA 
			where we got award. She sent me an offer to collaborate. Maybe we will see the 
			results next year. </p>
			<p> <strong>Can you share with us, what will be your next project? </strong></p>
			<p>My next movie is still a secret. It talks about very controversial people, 
			former terrorists. The only thing I can tell you is the title: “Scars.” The rest is a 
			mystery because of a woman’s safety. </p>
			<p> <strong>You couldn’t have given me a better reason to see it. I can’t wait. 
			Kacper, thank you so much for this interview. I wish you continuing 
			success! </strong></p>
			<p>Thank you! </p>
			<p>Joanna Derengowska </p>
			

